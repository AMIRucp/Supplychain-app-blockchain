const getWeb3 = () => {
  return new Web3(new Web3.providers.HttpProvider(infuraUrl));
};

const getContract = (abi) => {
  const web3 = getWeb3();
  return new web3.eth.Contract(abi, ContractAddress);
};

const buyProduct = async (walletAddress, productId, productPrice, abi) => {
  try {
    const web3 = getWeb3();
    const contract = getContract(abi);

    const transaction = await contract.methods.buyProduct(productId).send({
      from: walletAddress,
      value: web3.utils.toWei(productPrice.toString(), 'ether'), 
    });

    await transaction.wait();

    return transaction;
  } catch (error) {
    throw new Error('Error purchasing product');
  }
};

